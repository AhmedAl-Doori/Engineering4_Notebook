while True:
    a = float(input("Enter first number "))
    b = float(input("Enter second number "))
    print("a + b = {s}".format(s=a+b))
    print("a - b = {d}".format(d=a-b))
    print("a * b = {p}".format(p=a*b))
    print("a / b = {q}".format(q=a/b))
    print("a % b = {m}".format(m=a%b))
    print(" ")

